const ll N = 1e6;
void solve()
{
    ll n;
    cin >> n;
    multiset<ll> ans;

    /*
       This Problem got accepted by using multiset, but getting wrong answer when we use set in this
       case
    */

    ll k;
    for (ll i = 0; i < n; i++)
    {
        cin >> k;

        auto check = ans.upper_bound(k); // finding the tower having the element that is larger than the k

        if (check == ans.end()) // if it doesn't exist thenwe create a new tower
            ans.insert(k);
        else
        {
            // if there exist a satisfying tower, andding the k to that tower and
            // updating the top element

            ans.erase(check);
            ans.insert(k);
        }
    }
    cout << ans.size() << endl;
}
